SELECT Sid,Sname
FROM SALESMAN AS a
WHERE 1<
(SELECT COUNT(*)
FROM CUSTOMER2 AS c
WHERE c.Sid=a.Sid);

SELECT SALESMAN.Sid,Sname,Cname,Commission
FROM SALESMAN,CUSTOMER2
WHERE SALESMAN.City=CUSTOMER2.City
UNION
(SELECT Sid,Sname,'NO MATCH',Commission
FROM SALESMAN 
WHERE NOT City=ANY 
(SELECT City FROM CUSTOMER2))
ORDER BY 2 DESC;

CREATE VIEW SALES
AS SELECT b.Ord_Date,a.Sid 
FROM SALESMAN a,ORDERS b
WHERE a.Sid=b.Sid
AND b.Purchase_Amt=
(SELECT MAX(Purchase_Amt)
FROM ORDERS c
WHERE c.Ord_Date=b.Ord_Date);

